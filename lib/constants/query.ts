/**
 * @module lib/constants/query
 * TanStack Query の設定定数。QueryClient 生成・各フックのデフォルト値に使用する。
 * staleTime は「データの新鮮さ」要件に応じて 3 段階に分類する。
 */

// ---------------------------------------------------------------------------
// staleTime — データ種別ごとの鮮度タイムアウト
// ---------------------------------------------------------------------------

/**
 * フィード・通知など高頻度更新データの staleTime（30秒）。
 * フォアグラウンド復帰時やウィンドウフォーカス時に素早く再取得させるため短く設定する。
 */
export const STALE_TIME_REALTIME = 30 * 1000;

/**
 * 投稿詳細・ユーザープロフィールなど標準データの staleTime（5分）。
 * 参照頻度が高いが、秒単位の鮮度は不要なコンテンツに適用する。
 */
export const STALE_TIME_STANDARD = 5 * 60 * 1000;

/**
 * ジャンル一覧などマスタ系データの staleTime（30分）。
 * 変更頻度が極めて低いリソースに適用し、不要な再取得を抑制する。
 */
export const STALE_TIME_MASTER = 30 * 60 * 1000;

// ---------------------------------------------------------------------------
// gcTime — ガベージコレクション
// ---------------------------------------------------------------------------

/**
 * キャッシュが inactive になってから削除するまでの時間（10分）。
 * React Query v5 のデフォルト（5分）より長めに設定し、
 * 画面遷移後の「戻る」操作でキャッシュを再利用しやすくする。
 */
export const GC_TIME = 10 * 60 * 1000;

// ---------------------------------------------------------------------------
// リトライ
// ---------------------------------------------------------------------------

/**
 * 5xx・ネットワークエラー時の最大リトライ回数。
 * 4xx は仕様上リトライしない（api-client.md / error-handling.md）。
 * 指数バックオフと組み合わせるため過大にしない。
 */
export const RETRY_COUNT = 2;

/**
 * リトライ間隔の基準値（ミリ秒）。
 * 指数バックオフ: attempt 1 → 1秒、attempt 2 → 2秒（RETRY_COUNT=2 の場合）。
 */
export const RETRY_DELAY_BASE_MS = 1000;

// ---------------------------------------------------------------------------
// タイムアウト
// ---------------------------------------------------------------------------

/**
 * API リクエストのタイムアウト値（10秒）。
 * モバイル回線を考慮し、Web 版（5秒）より緩めに設定する。
 */
export const REQUEST_TIMEOUT_MS = 10 * 1000;
